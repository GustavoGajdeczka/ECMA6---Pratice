// Valores padrões dos parametros ////////////////////////////////
// Agregação dos argumentos restantes em um único parâmetro de funções variáveis.
////////////////////////////////////////////////////////////////
// ECMA6 ///////////////////////////////////////////////////////
function foo(x, y, ...a){
    return (x + y) * a.length
}
foo(1, 2, "Ola Mundo", true, 7) === 9 // True
////////////////////////////////////////////////////////////////
// ECMA5 ///////////////////////////////////////////////////////
function foo(x, y){
    var a = Array.prototype.slice.call(arguments, 2);
    return (x + y) * a.length
}
foo(1, 2, "ola mundo", true, 7) === 9 // True